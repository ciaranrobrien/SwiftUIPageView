public enum VerticalPageAlignment: CaseIterable, Hashable {
    
    /// A guide marking the top edge of the page.
    case top
    
    /// A guide marking the vertical center of the page.
    case center
    
    /// A guide marking the bottom edge of the page.
    case bottom
}
